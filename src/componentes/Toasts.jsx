import { toast, Bounce } from 'react-toastify';

export const mostrarError = (mensaje) => {
  toast.error(mensaje, {
    position: "top-center",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "dark",
    transition: Bounce,
  });
};


